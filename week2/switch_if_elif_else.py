# Python does not have switch, we use if-elif-else

# Example 1
choice = 2
if choice == 1:
    print("Start")
elif choice == 2:
    print("Settings")
elif choice == 3:
    print("Exit")
else:
    print("Invalid choice")

# Example 2
day = "Mon"
if day == "Mon":
    print("Monday")
elif day == "Tue":
    print("Tuesday")
else:
    print("Other day")

# Example 3
num = 5
if num == 1:
    print("One")
elif num == 5:
    print("Five")
else:
    print("Unknown")

# Example 4
grade = "B"
if grade == "A":
    print("Excellent")
elif grade == "B":
    print("Good")
else:
    print("Needs improvement")

# Example 5
level = 3
if level == 1:
    print("Easy")
elif level == 2:
    print("Medium")
elif level == 3:
    print("Hard")
else:
    print("Unknown")
