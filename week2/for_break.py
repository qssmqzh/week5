# for loop with break

# Example 1
for i in range(5):
    if i == 3:
        break
    print(i)

# Example 2
for char in "hello":
    if char == "l":
        break
    print(char)

# Example 3
for x in range(10):
    if x == 7:
        break
    print(x)

# Example 4
nums = [1, 2, 3, 4]
for n in nums:
    if n == 3:
        break
    print(n)

# Example 5
for i in range(1, 6):
    if i == 4:
        break
    print(i)
