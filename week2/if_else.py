# if else statement

# Example 1
age = 16
if age >= 18:
    print("Adult")
else:
    print("Minor")

# Example 2
x = 3
if x % 2 == 0:
    print("Even")
else:
    print("Odd")

# Example 3
password = "1234"
if password == "1234":
    print("Access granted")
else:
    print("Access denied")

# Example 4
is_raining = False
if is_raining:
    print("Take umbrella")
else:
    print("No umbrella needed")

# Example 5
score = 50
if score >= 60:
    print("Passed")
else:
    print("Failed")
