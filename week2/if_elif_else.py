# if elif else statement

# Example 1
score = 85
if score >= 90:
    print("A")
elif score >= 80:
    print("B")
else:
    print("C")

# Example 2
x = 0
if x > 0:
    print("Positive")
elif x < 0:
    print("Negative")
else:
    print("Zero")

# Example 3
age = 12
if age < 13:
    print("Child")
elif age < 20:
    print("Teenager")
else:
    print("Adult")

# Example 4
day = 3
if day == 1:
    print("Monday")
elif day == 2:
    print("Tuesday")
else:
    print("Another day")

# Example 5
temp = 15
if temp > 25:
    print("Hot")
elif temp > 10:
    print("Warm")
else:
    print("Cold")
