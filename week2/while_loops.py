# While loops

# Example 1
i = 1
while i <= 5:
    print(i)
    i += 1

# Example 2
x = 0
while x < 3:
    print("Hello")
    x += 1

# Example 3
count = 5
while count > 0:
    print(count)
    count -= 1

# Example 4
num = 2
while num <= 10:
    print(num)
    num += 2

# Example 5
a = 1
while a <= 4:
    print(a * a)
    a += 1
